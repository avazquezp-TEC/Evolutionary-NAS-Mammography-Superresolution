# 5) Training dataset
#    Random crop → Augmentation → Full Med-BSR degradation (random per patch)
# ===========================================================================

def _stateless_augment(hr: tf.Tensor, s: tf.Tensor) -> tf.Tensor:
    """
    Apply stateless (reproducible) geometric and photometric augmentation.

    Operations applied in order:
      1. Random horizontal flip
      2. Random vertical flip
      3. Random 90° rotation (k ∈ {0, 1, 2, 3})
      4. Random brightness jitter  (±10 %)
      5. Random contrast jitter    (×[0.90, 1.10])

    Using stateless ops ensures that the same patch index always produces
    the same augmentation, making training reproducible given a fixed seed.

    Parameters
    ----------
    hr : HR patch tensor, float32 [patch_size, patch_size, 3].
    s  : 2-element int32 seed tensor derived from the global patch counter.
    """
    hr = tf.image.stateless_random_flip_left_right(hr, seed=s)
    hr = tf.image.stateless_random_flip_up_down(hr, seed=s + 1)
    k  = tf.random.stateless_uniform([], seed=s + 2, minval=0, maxval=4, dtype=tf.int32)
    hr = tf.image.rot90(hr, k=k)
    hr = tf.image.stateless_random_brightness(hr, max_delta=0.10, seed=s + 3)
    hr = tf.image.stateless_random_contrast(hr, lower=0.90, upper=1.10, seed=s + 4)
    return tf.clip_by_value(hr, 0.0, 1.0)


def make_train_dataset_randomcrop(
    directory: str, cfg: DataCfg, seed: int
) -> tf.data.Dataset:
    """
    Build the infinite training dataset with on-the-fly Med-BSR degradation.

    Pipeline steps
    --------------
    1. Shuffle image paths and repeat infinitely.
    2. Decode each image to float32 [H, W, 3].
    3. Repeat each image 12 times to increase crop diversity on the small dataset.
    4. Extract a random HR crop of shape [patch_size, patch_size, 3].
    5. Apply stateless augmentation (flips, rotation, brightness, contrast).
    6. Apply full Med-BSR degradation via tf.py_function:
         - Blur  : B_iso or B_aniso, parameters sampled uniformly per patch.
         - Noise : additive Gaussian, type sampled with probabilities 2/5·2/5·1/5.
         - Down  : MST method index = patch_counter % 6, cycling through all 6 methods.
         - Order : randomly shuffled among the three factors.
    7. Batch and prefetch.

    The global patch counter (tf.data.experimental.Counter) serves as the per-patch
    seed, so blur parameters, noise level, MST method, and augmentation are all
    deterministic given the seed argument, while still varying across patches.

    Parameters
    ----------
    directory : Path to the training image directory.
    cfg       : DataCfg instance with ratio, patch_size, overlap, batch_size, grayscale.
    seed      : Base random seed for reproducibility.
    """
    paths = list_images(directory)
    if not paths:
        raise ValueError(f"No image files found in: {directory}")

    ps    = cfg.patch_size
    r     = cfg.ratio
    lr_ps = ps // r
    gray  = cfg.grayscale

    # One degradation function per tf.data worker thread (thread-safe via py_function).
    _degrade = make_degradation_fn(scale=r, seed=seed)

    # ------------------------------------------------------------------
    # Build the tf.data pipeline
    # ------------------------------------------------------------------
    ds = tf.data.Dataset.from_tensor_slices(paths)
    ds = ds.shuffle(len(paths), seed=seed, reshuffle_each_iteration=True)
    ds = ds.repeat()
    ds = ds.map(lambda p: _decode_mammo_tf(p, gray), num_parallel_calls=AUTOTUNE)

    # Repeat each decoded image to extract multiple crops without re-reading disk.
    # With only ~480 training images, 12 crops per image gives 5 760 patches/epoch.
    repeats_per_image = 12
    ds = ds.flat_map(
        lambda img: tf.data.Dataset.from_tensors(img).repeat(repeats_per_image)
    )

    # Zip with a global counter used as a per-patch seed.
    counter = tf.data.experimental.Counter(start=seed)
    ds = tf.data.Dataset.zip((ds, counter))

    def _make_pair(img: tf.Tensor, idx: tf.Tensor):
        """
        Extract one (LR, HR) training pair from a decoded image.

        Skips the image if it is smaller than the patch size (returns zeros
        and an `ok=False` flag that the subsequent filter step removes).
        """
        h  = tf.shape(img)[0]
        w  = tf.shape(img)[1]
        ok = tf.logical_and(h >= ps, w >= ps)

        # Per-patch 2-element seed: [global_seed, patch_counter mod 2^30].
        s = tf.stack([tf.cast(seed, tf.int32), tf.cast(idx % (2 ** 30), tf.int32)])

        def _do():
            # Step 1: random crop to HR patch size.
            hr = tf.image.stateless_random_crop(img, [ps, ps, 3], seed=s)

            # Step 2: geometric and photometric augmentation.
            hr = _stateless_augment(hr, s)
            hr = tf.clip_by_value(hr, 0.0, 1.0)

            # Step 3: Med-BSR degradation via PIL (outside the TF graph).
            # The MST method cycles as idx % 6 so all 6 methods are used equally.
            method_idx = tf.cast(idx % N_MST, tf.int32)

            def _py_degrade(hr_t: tf.Tensor, midx_t: tf.Tensor) -> tf.Tensor:
                lr_np = _degrade(hr_t.numpy(), int(midx_t.numpy()))
                return lr_np.astype(np.float32)

            lr = tf.py_function(_py_degrade, [hr, method_idx], tf.float32)
            lr.set_shape([lr_ps, lr_ps, 3])
            lr = tf.clip_by_value(lr, 0.0, 1.0)
            return lr, hr

        # Fall-back zero tensors for images that are too small (filtered out below).
        lr0 = tf.zeros([lr_ps, lr_ps, 3], tf.float32)
        hr0 = tf.zeros([ps,    ps,    3], tf.float32)
        lr, hr = tf.cond(ok, _do, lambda: (lr0, hr0))
        return lr, hr, ok

    ds = ds.map(_make_pair, num_parallel_calls=AUTOTUNE)
    ds = ds.filter(lambda lr, hr, ok: ok)           # discard under-sized images
    ds = ds.map(lambda lr, hr, ok: (lr, hr), num_parallel_calls=AUTOTUNE)
    ds = ds.batch(cfg.batch_size, drop_remainder=True)
    ds = ds.prefetch(AUTOTUNE)
    return ds


# ===========================================================================
# 6) Validation dataset
#    Deterministic sliding-window extraction + Med-BSR degradation
#    Fixed order blur → noise → downsample; MST method in round-robin
# ===========================================================================

def _val_generator(paths: List[str], cfg: DataCfg, scale: int):
    """
    Python generator that yields (lr_batch, hr_batch) pairs for validation.

    Design decisions
    ----------------
    - **Sliding window**: patches are extracted with a fixed stride
      (patch_size × (1 - overlap)) so the same patches are used every epoch.
    - **Fixed degradation order** (blur → noise → downsample): ensures that
      PSNR measurements are comparable across epochs and experiments.
    - **Round-robin MST**: patch_idx % 6 cycles through all six downsampling
      methods so the validation metric reflects average model performance across
      all degradation types, not just one method.
    - **Deterministic seed per patch** (seed = patch_idx): all degradation
      parameters (sigma, kernel size, noise level) are fully reproducible.

    Yields
    ------
    (lr_batch, hr_batch) : float32 numpy arrays of shape
                           (batch_size, lr_ps, lr_ps, 3) and (batch_size, ps, ps, 3).
    """
    ps          = cfg.patch_size
    lr_ps       = ps // cfg.ratio
    step        = max(1, int(ps * (1.0 - cfg.overlap)))
    gray        = cfg.grayscale
    ORDER_FIXED = [0, 1, 2]   # 0=blur, 1=noise, 2=downsample — fixed for reproducibility

    batch_lr: List[np.ndarray] = []
    batch_hr: List[np.ndarray] = []
    patch_idx = 0

    for path in paths:
        try:
            img = _decode_mammo_pil(path, gray)
        except Exception:
            continue

        h, w = img.shape[:2]
        if h < ps or w < ps:
            continue

        for y in range(0, h - ps + 1, step):
            for x in range(0, w - ps + 1, step):
                hr = img[y:y + ps, x:x + ps, :].astype(np.float32)

                # Deterministic Med-BSR degradation: seed = patch index ensures
                # every patch gets the same degradation parameters across epochs.
                deg        = MammoDegradation(scale=scale, seed=patch_idx)
                method_idx = patch_idx % N_MST
                lr         = deg(hr, method_idx=method_idx, order=ORDER_FIXED)

                batch_lr.append(lr)
                batch_hr.append(hr)
                patch_idx += 1

                # Yield a full batch and reset accumulators.
                if len(batch_lr) == cfg.batch_size:
                    yield (
                        np.asarray(batch_lr, dtype=np.float32),
                        np.asarray(batch_hr, dtype=np.float32),
                    )
                    batch_lr, batch_hr = [], []


def make_val_dataset_deterministic(
    directory: str, cfg: DataCfg
) -> Tuple[tf.data.Dataset, int]:
    """
    Wrap the validation generator as a tf.data.Dataset.

    The dataset is bounded to `val_steps` batches (computed from the total
    number of patches in the directory) and prefetched for efficiency.

    Returns
    -------
    ds        : Bounded, prefetched tf.data.Dataset yielding (LR, HR) pairs.
    val_steps : Number of batches per validation pass.
    """
    paths = list_images(directory)
    if not paths:
        raise ValueError(f"No image files found in: {directory}")

    val_steps, _, _ = compute_steps_per_epoch(directory, cfg)
    ps    = cfg.patch_size
    lr_ps = ps // cfg.ratio
    scale = cfg.ratio

    ds = tf.data.Dataset.from_generator(
        lambda: _val_generator(paths, cfg, scale),
        output_signature=(
            tf.TensorSpec(shape=(cfg.batch_size, lr_ps, lr_ps, 3), dtype=tf.float32),
            tf.TensorSpec(shape=(cfg.batch_size, ps,    ps,    3), dtype=tf.float32),
        ),
    )
    ds = ds.take(val_steps).prefetch(AUTOTUNE)
    return ds, val_steps


