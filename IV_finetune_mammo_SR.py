@tf.keras.utils.register_keras_serializable(package="sr")
def psnr(y_true, y_pred):
    """
    Peak Signal-to-Noise Ratio computed in float32 on the [0, 1] range.

    Registered as a Keras serialisable object so it can be saved and reloaded
    as part of the model's compiled state.
    """
    y_true = tf.cast(tf.clip_by_value(y_true, 0.0, 1.0), tf.float32)
    y_pred = tf.cast(tf.clip_by_value(y_pred, 0.0, 1.0), tf.float32)
    return tf.image.psnr(y_true, y_pred, max_val=1.0)


