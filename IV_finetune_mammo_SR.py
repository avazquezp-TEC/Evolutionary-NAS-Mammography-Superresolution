def _is_image_file(path: str) -> bool:
    """Return True if the file extension is a supported image format."""
    return path.lower().endswith(ALLOWED_EXTS)


def list_images(directory: str) -> List[str]:
    """Return a sorted list of absolute paths to all image files in `directory`."""
    return sorted(
        os.path.join(directory, name)
        for name in os.listdir(directory)
        if os.path.isfile(os.path.join(directory, name)) and _is_image_file(name)
    )


def get_image_hw(path: str) -> Optional[Tuple[int, int]]:
    """
    Return (height, width) for the image at `path`, or None if it cannot be read.
    Uses PIL so it works with all supported formats without loading pixel data.
    """
    try:
        w, h = Image.open(path).size
        return h, w
    except Exception:
        return None


def count_patches(h: int, w: int, patch_size: int, overlap: float) -> int:
    """
    Count how many non-overlapping (or partially overlapping) patches of size
    `patch_size × patch_size` fit in an image of height `h` and width `w`.

    The sliding-window step is: step = patch_size × (1 - overlap).
    Returns 0 if the image is smaller than one patch in either dimension.
    """
    if h < patch_size or w < patch_size:
        return 0
    step = max(1, int(patch_size * (1.0 - overlap)))
    return ((h - patch_size) // step + 1) * ((w - patch_size) // step + 1)


def compute_steps_per_epoch(directory: str, cfg: DataCfg) -> Tuple[int, int, int]:
    """
    Estimate training/validation steps per epoch for all images in `directory`.

    Returns
    -------
    steps         : Number of full batches (total_patches // batch_size).
    total_patches : Total patch count across all valid images.
    used          : Number of images that yielded at least one patch.
    """
    files = list_images(directory)
    total_patches, used = 0, 0
    for path in files:
        hw = get_image_hw(path)
        if hw is None:
            continue
        n = count_patches(hw[0], hw[1], cfg.patch_size, cfg.overlap)
        if n > 0:
            total_patches += n
            used += 1
    steps = max(1, total_patches // cfg.batch_size)
    return steps, total_patches, used


