def compile_model(model, lr, wd: float = 1e-8, eps: float = 1e-7) -> None:
    """
    Compile the SR model with AdamW optimiser and MSE loss.

    A very small weight decay (1e-8) and epsilon (1e-7) are used to avoid
    numerical instability at the low learning rates typical of fine-tuning.
    steps_per_execution=50 fuses 50 gradient steps into a single XLA call,
    reducing Python overhead significantly on GPU.
    """
    opt = tf.keras.optimizers.AdamW(learning_rate=lr, weight_decay=wd, epsilon=eps)
    model.compile(
        optimizer=opt,
        loss=tf.keras.losses.MeanSquaredError(),
        metrics=[psnr],
        steps_per_execution=50,
    )


def cosine_lr_schedule(initial_lr: float, total_steps: int):
    """
    Cosine decay schedule from `initial_lr` to 0 over `total_steps` steps.

    Cosine decay tends to outperform a fixed LR for domain adaptation because
    it starts with the same learning rate (allowing quick adaptation) and
    smoothly reduces it, preventing overshooting near convergence.
    """
    return tf.keras.optimizers.schedules.CosineDecay(
        initial_learning_rate=initial_lr,
        decay_steps=total_steps,
        alpha=0.0,
    )


def make_callbacks(
    stage_dir: str,
    stage_name: str,
    es_patience: int,
    es_min_delta: float,
) -> list:
    """
    Build the standard callback set for fine-tuning.

    Callbacks
    ---------
    CSVLogger        : Appends epoch-level metrics to a CSV log file.
    ModelCheckpoint  : Saves the best model (by val_psnr) and the last checkpoint.
    ReduceLROnPlateau: Halves the LR when val_psnr stagnates for patience//4 epochs.
    EarlyStopping    : Stops training and restores best weights when val_psnr
                       fails to improve by `es_min_delta` for `es_patience` epochs.
    TerminateOnNaN   : Immediately stops training if a NaN loss is detected.
    """
    os.makedirs(stage_dir, exist_ok=True)
    return [
        tf.keras.callbacks.CSVLogger(
            os.path.join(stage_dir, f"{stage_name}_log.csv"), append=False
        ),
        tf.keras.callbacks.ModelCheckpoint(
            filepath=os.path.join(stage_dir, f"{stage_name}_best.keras"),
            monitor="val_psnr", mode="max", save_best_only=True, verbose=1,
        ),
        tf.keras.callbacks.ModelCheckpoint(
            filepath=os.path.join(stage_dir, f"{stage_name}_last.keras"),
            save_best_only=False, verbose=0,
        ),
        tf.keras.callbacks.ReduceLROnPlateau(
            monitor="val_psnr", mode="max",
            factor=0.5, patience=max(2, es_patience // 4),
            min_lr=1e-7, verbose=1,
        ),
        tf.keras.callbacks.EarlyStopping(
            monitor="val_psnr", mode="max",
            patience=es_patience, min_delta=es_min_delta,
            restore_best_weights=True, verbose=1,
        ),
        tf.keras.callbacks.TerminateOnNaN(),
    ]


