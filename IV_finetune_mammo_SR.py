"""
finetune_mammo_SR.py
====================
Stage-2 fine-tuning of a super-resolution model for mammography images.

Starting point : a .keras model pre-trained on DIV2K (Stage 2 of TrainSR_GPU.py).
Target domain  : mammography images from the mammo-bench dataset.
Train directory: DATASET/mammo_train/  (generated by 01_select_mammo_images.py)
Val directory  : DATASET/mammo_val/

LR degradation pipeline — Med-BSR (Shao et al., IET Image Processing 2023)
combined with the 6-method MST downsampling framework:

  BLUR  : isotropic Gaussian (B_iso) or anisotropic Gaussian (B_aniso) — sampled per patch
  NOISE : additive Gaussian with 3 covariance variants (prob 2/5 · 2/5 · 1/5)
  DOWN  : one of 6 MST methods (Nearest · Bilinear · Bicubic · Lanczos · Box · Hamming)
  ORDER : the three factors are applied in a randomly shuffled order per patch
          (random select/combine strategy — 6 possible orderings × 6 MST methods = 36 paths)

  TRAIN : all parameters and ordering are fully random per patch (via tf.py_function + PIL)
  VAL   : deterministic round-robin over the 6 MST methods, fixed order blur→noise→down
          → uniform coverage of all degradation types and full reproducibility across epochs

Quick start:
  # Fine-tune a single model
  python IV_finetune_mammo_SR.py \
      --model_path outputs4x/.../stage2_p128_best.keras \
      --upscale 4 --grayscale

  # Fine-tune all NAS genes in a directory
  python IV_finetune_mammo_SR.py \
      --genes_dir outputs4x/a100_fast_bestpsnr_20260519_131528 \
      --out_dir   outputs \
      --upscale 4 --grayscale

Dependencies:
  tensorflow >= 2.13, Pillow, numpy, scipy
  mammo_degradation.py  (must be in the same directory)

"""

