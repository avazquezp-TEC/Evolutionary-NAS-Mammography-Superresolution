def find_best_models(genes_dir: str) -> List[Tuple[str, str]]:
    """
    Scan a NAS output directory for saved gene models.

    Looks for Stage-2 checkpoints first (stage2_p128_best.keras); falls back
    to Stage-1 checkpoints (stage1_p64_best.keras) if Stage 2 is not found.

    Parameters
    ----------
    genes_dir : Root directory containing gene_XXX/ subdirectories.

    Returns
    -------
    List of (gene_name, model_path) tuples sorted by gene name.
    """
    pattern_st2 = os.path.join(genes_dir, "gene_*", "stage2_p128_best.keras")
    pattern_st1 = os.path.join(genes_dir, "gene_*", "stage1_p64_best.keras")
    found = sorted(glob.glob(pattern_st2)) or sorted(glob.glob(pattern_st1))
    return [(os.path.basename(os.path.dirname(p)), p) for p in found]


# ===========================================================================
# 10) Single-model fine-tuning
# ===========================================================================

def finetune_one(
    model_path: str,
    train_ds: tf.data.Dataset,
    val_ds: tf.data.Dataset,
    train_steps: int,
    val_steps: int,
    out_dir: str,
    model_name: str,
    lr: float,
    max_epochs: int,
    es_patience: int,
    es_min_delta: float,
    strategy: tf.distribute.Strategy,
    use_cosine_lr: bool,
) -> Tuple[float, str, int]:
    """
    Load a pre-trained SR model and fine-tune it on the mammography dataset.

    The model is loaded inside the distribution strategy scope so that its
    variables are placed on the correct devices from the start.

    Parameters
    ----------
    model_path    : Path to the source .keras model file.
    train_ds      : Infinite training dataset (LR, HR) pairs.
    val_ds        : Bounded validation dataset (LR, HR) pairs.
    train_steps   : Number of batches per training epoch.
    val_steps     : Number of batches per validation pass.
    out_dir       : Root output directory; a subdirectory `model_name/` is created.
    model_name    : Name used for the output subdirectory and checkpoint file names.
    lr            : Initial learning rate.
    max_epochs    : Maximum number of training epochs (EarlyStopping may stop sooner).
    es_patience   : EarlyStopping patience in epochs.
    es_min_delta  : Minimum val_psnr improvement to reset the EarlyStopping counter.
    strategy      : Distribution strategy (single-GPU or MirroredStrategy).
    use_cosine_lr : If True, use cosine decay; otherwise use fixed LR + ReduceLROnPlateau.

    Returns
    -------
    best_psnr    : Best validation PSNR achieved during training (dB).
    best_path    : Path to the saved best checkpoint (.keras file).
    param_count  : Total number of trainable parameters in the model.
    """
    print(f"\n  Loading model: {model_path}")

    with strategy.scope():
        model = tf.keras.models.load_model(model_path, compile=False)

        if use_cosine_lr:
            total_steps = max_epochs * train_steps
            compile_model(model, lr=cosine_lr_schedule(lr, total_steps))
            print(f"  LR schedule: cosine decay {lr} → 0 over {total_steps} steps")
        else:
            compile_model(model, lr=lr)
            print(f"  LR: fixed {lr}  (+ReduceLROnPlateau)")

    param_count = int(model.count_params())
    print(f"  Parameters: {param_count:,}")

    stage_dir = os.path.join(out_dir, model_name)
    os.makedirs(stage_dir, exist_ok=True)

    history = model.fit(
        train_ds,
        validation_data=val_ds,
        epochs=max_epochs,
        steps_per_epoch=train_steps,
        validation_steps=val_steps,
        callbacks=make_callbacks(stage_dir, "mammo_ft", es_patience, es_min_delta),
        verbose=1,
    )

    best_psnr = float(np.nanmax(history.history.get("val_psnr", [np.nan])))
    best_path = os.path.join(stage_dir, "mammo_ft_best.keras")
    print(f"  Best val_psnr: {best_psnr:.4f} dB  →  {best_path}")

    # Release GPU memory before the next model is loaded.
    tf.keras.backend.clear_session()
    return best_psnr, best_path, param_count


# ===========================================================================
# 11) CLI argument parsing
# ===========================================================================

def parse_args() -> argparse.Namespace:
    p = argparse.ArgumentParser(
        description=(
            "Fine-tune a super-resolution model on mammography images "
            "using Med-BSR degradation and 6-method MST downsampling."
        )
    )

    # --- Data paths ----------------------------------------------------------
    p.add_argument("--train_dir", default="DATASET/mammo_train",
                   help="Directory with training HR images.")
    p.add_argument("--val_dir",   default="DATASET/mammo_val",
                   help="Directory with validation HR images.")
    p.add_argument("--out_dir",   default="outputs",
                   help="Root output directory for checkpoints and logs.")

    # --- Model selection (mutually exclusive) --------------------------------
    g = p.add_mutually_exclusive_group(required=True)
    g.add_argument("--model_path", metavar="PATH",
                   help="Path to a single .keras model to fine-tune.")
    g.add_argument("--genes_dir",  metavar="DIR",
                   help="NAS output directory; all gene_XXX/ models are fine-tuned.")

    # --- Training hyperparameters --------------------------------------------
    p.add_argument("--lr",           type=float, default=1e-5,
                   help="Initial learning rate (default: 1e-5).")
    p.add_argument("--max_epochs",   type=int,   default=100,
                   help="Maximum training epochs (default: 100).")
    p.add_argument("--batch_size",   type=int,   default=32,
                   help="Batch size (default: 32).")
    p.add_argument("--patch_size",   type=int,   default=128,
                   help="HR patch size in pixels (default: 128).")
    p.add_argument("--upscale",      type=int,   default=4,
                   help="Upscale factor: 2 or 4. Affects blur sigma ranges (default: 4).")
    p.add_argument("--overlap",      type=float, default=0.1,
                   help="Sliding-window overlap for validation patches (default: 0.1).")
    p.add_argument("--es_patience",  type=int,   default=12,
                   help="EarlyStopping patience in epochs (default: 12).")
    p.add_argument("--es_min_delta", type=float, default=0.01,
                   help="Minimum val_psnr improvement to reset EarlyStopping (default: 0.01).")
    p.add_argument("--cosine_lr",    action="store_true",
                   help="Use cosine decay LR schedule instead of fixed LR + ReduceLROnPlateau.")

    # --- Image options -------------------------------------------------------
    p.add_argument("--grayscale", action="store_true",
                   help="Load images as grayscale and replicate to 3 channels.")

    # --- Runtime options -----------------------------------------------------
    p.add_argument("--cpu",    action="store_true",
                   help="Force CPU execution (for debugging on machines without a GPU).")
    p.add_argument("--seed",   type=int, default=42,
                   help="Global random seed for reproducibility (default: 42).")
    p.add_argument("--no_xla", action="store_true",
                   help="Disable XLA JIT compilation.")

    return p.parse_args()


# ===========================================================================
# 12) Entry point
# ===========================================================================

def main() -> None:
    args = parse_args()

    # --- Runtime setup -------------------------------------------------------
    configure_threads()
    configure_runtime(cpu_mode=args.cpu)
    configure_mixed_precision(cpu_mode=args.cpu)
    if not args.no_xla:
        enable_xla()

    print(f"TensorFlow : {tf.__version__}")
    print(f"CPU mode   : {args.cpu}")
    print(f"GPUs       : {len(tf.config.list_logical_devices('GPU'))}")
    print(f"Grayscale  : {args.grayscale}")
    print(f"MST ({N_MST}): {MST_NAMES}")
    print(f"Degradation: Med-BSR blur+noise+down  (scale ×{args.upscale})")

    # Global seeds — cover Python random, NumPy, and TensorFlow.
    random.seed(args.seed)
    np.random.seed(args.seed)
    tf.random.set_seed(args.seed)

    # --- Data configuration --------------------------------------------------
    cfg = DataCfg(
        ratio      = args.upscale,
        patch_size = args.patch_size,
        overlap    = args.overlap,
        batch_size = args.batch_size,
        grayscale  = args.grayscale,
    )

    print(f"\n[DATA] Building pipelines ...")
    print(f"  Train : {args.train_dir}")
    print(f"  Val   : {args.val_dir}")

    train_steps, train_patches, train_imgs = compute_steps_per_epoch(args.train_dir, cfg)
    val_steps,   val_patches,   val_imgs   = compute_steps_per_epoch(args.val_dir,   cfg)

    print(f"  Train  imgs={train_imgs}  patches={train_patches:,}  steps/epoch={train_steps:,}")
    print(f"  Val    imgs={val_imgs}    patches={val_patches:,}    steps/epoch={val_steps:,}")
    print(f"  Train degradation : Med-BSR fully random (blur+noise+down, shuffled order)")
    print(f"  Val   degradation : Med-BSR deterministic (blur→noise→down, MST round-robin)")

    train_ds          = make_train_dataset_randomcrop(args.train_dir, cfg, seed=args.seed + 11)
    val_ds, val_steps = make_val_dataset_deterministic(args.val_dir, cfg)

    # --- Output directories --------------------------------------------------
    run_id = datetime.datetime.now().strftime("%Y%m%d_%H%M%S")
    root   = os.path.join(f"{args.out_dir}{args.upscale}x", f"mammo_blindSR{run_id}")
    os.makedirs(root, exist_ok=True)

    strategy = get_strategy_auto()
    print(f"[STRATEGY] {type(strategy).__name__}  replicas: {strategy.num_replicas_in_sync}")

    # --- Resolve model list --------------------------------------------------
    if args.model_path:
        models_to_run = [("model", args.model_path)]
    else:
        models_to_run = find_best_models(args.genes_dir)
        if not models_to_run:
            print(f"[ERROR] No .keras models found in: {args.genes_dir}")
            return
        print(f"\n[GENES] {len(models_to_run)} model(s) found for fine-tuning")

    # --- Summary CSV ---------------------------------------------------------
    summary_path = os.path.join(root, f"finetune_summary_{run_id}.csv")
    fieldnames   = ["model_name", "source_model", "params", "best_val_psnr", "ft_model_path", "Seconds"]
    with open(summary_path, "w", newline="", encoding="utf-8") as f:
        csv.DictWriter(f, fieldnames=fieldnames).writeheader()

    # --- Fine-tuning loop ----------------------------------------------------
    for model_name, model_path in models_to_run:
        print(f"\n{'='*60}")
        print(f"  Fine-tuning: {model_name}")
        print(f"{'='*60}")
        tic = time.perf_counter()
        best_psnr, ft_model_path, params = finetune_one(
            model_path    = model_path,
            train_ds      = train_ds,
            val_ds        = val_ds,
            train_steps   = train_steps,
            val_steps     = val_steps,
            out_dir       = root,
            model_name    = model_name,
            lr            = args.lr,
            max_epochs    = args.max_epochs,
            es_patience   = args.es_patience,
            es_min_delta  = args.es_min_delta,
            strategy      = strategy,
            use_cosine_lr = args.cosine_lr,
        )
        toc = time.perf_counter()
        # Append result row to the summary CSV.
        with open(summary_path, "a", newline="", encoding="utf-8") as f:
            csv.DictWriter(f, fieldnames=fieldnames).writerow({
                "model_name":    model_name,
                "source_model":  model_path,
                "params":        params,
                "best_val_psnr": best_psnr,
                "ft_model_path": ft_model_path,
                "Seconds":       (toc - tic)
            })

    print(f"\n{'='*60}")
    print(f"  Done. Results saved to: {root}")
    print(f"  Summary CSV: {summary_path}")
    print(f"{'='*60}\n")
    print(f"[time] Elapsed time: {toc - tic:.6f} seconds\n")

if __name__ == "__main__":
    main()
