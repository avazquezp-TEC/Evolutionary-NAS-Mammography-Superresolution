@tf.keras.utils.register_keras_serializable(package="sr")
class PixelShuffle(tf.keras.layers.Layer):
    """
    Sub-pixel convolution (PixelShuffle) implemented via tf.nn.depth_to_space.

    Rearranges elements from the channel dimension into spatial dimensions,
    effectively upscaling the feature map by `upscale_factor`.
    Required to deserialise models saved by the original TrainSR_GPU.py script.
    """

    def __init__(self, upscale_factor: int, **kwargs):
        super().__init__(**kwargs)
        self.upscale_factor = int(upscale_factor)

    def call(self, x):
        return tf.nn.depth_to_space(x, self.upscale_factor)

    def get_config(self):
        cfg = super().get_config()
        cfg.update({"upscale_factor": self.upscale_factor})
        return cfg


