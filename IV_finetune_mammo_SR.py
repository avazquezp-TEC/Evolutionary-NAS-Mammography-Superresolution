@dataclass
class DataCfg:
    """
    Central configuration for the data pipeline.

    Attributes
    ----------
    ratio      : Upscale factor (2 or 4). Sets the LR patch size as patch_size // ratio.
    patch_size : HR patch size in pixels (both height and width).
    overlap    : Sliding-window overlap fraction used during validation (0 = no overlap).
    batch_size : Number of (LR, HR) pairs per batch.
    grayscale  : If True, images are loaded as single-channel and replicated to 3 channels.
                 Mammograms are inherently grayscale; replication lets the model architecture
                 (which expects 3-channel input) work without modification.
    """
    ratio:      int   = 4
    patch_size: int   = 128
    overlap:    float = 0.1
    batch_size: int   = 32
    grayscale:  bool  = True


