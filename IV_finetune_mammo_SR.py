def configure_runtime(cpu_mode: bool) -> None:
    """
    Configure GPU visibility and memory growth.

    If `cpu_mode` is True, all GPUs are hidden and TensorFlow runs on CPU only.
    Otherwise, memory growth is enabled on every visible GPU to avoid allocating
    the full VRAM upfront (prevents OOM errors when sharing the GPU).
    """
    if cpu_mode:
        tf.config.set_visible_devices([], "GPU")
        return

    gpus = tf.config.list_physical_devices("GPU")
    if gpus:
        try:
            for g in gpus:
                tf.config.experimental.set_memory_growth(g, True)
            print("[GPU] memory growth enabled")
        except Exception as exc:
            print(f"[GPU] warning: {exc}")
            print("[Runtime] WARNING: GPU requested but unavailable — falling back to CPU")


def configure_threads() -> None:
    """
    Let TensorFlow choose the optimal thread count automatically (value 0).
    Prevents over-subscription on machines with many CPU cores.
    """
    try:
        tf.config.threading.set_intra_op_parallelism_threads(0)
        tf.config.threading.set_inter_op_parallelism_threads(0)
    except Exception:
        pass


def configure_mixed_precision(cpu_mode: bool) -> None:
    """
    Enable mixed-precision training (float16 compute + float32 weights) on GPU.
    On CPU or when `cpu_mode=True`, float32 is used throughout to avoid
    numerical issues on hardware that lacks native float16 support.
    """
    try:
        from tensorflow.keras import mixed_precision
        policy = "float32" if cpu_mode else "mixed_float16"
        mixed_precision.set_global_policy(policy)
        print("[MP] policy:", mixed_precision.global_policy())
    except Exception as exc:
        print(f"[MP] warning: {exc}")


def enable_xla() -> None:
    """
    Enable XLA (Accelerated Linear Algebra) JIT compilation.
    XLA fuses TensorFlow ops into optimised kernels, reducing memory bandwidth
    and improving throughput — especially useful with the conv-heavy SR models.
    Disabled automatically on macOS (MPS / Apple Silicon compatibility).
    """
    try:
        if platform.system() != "Darwin":
            tf.config.optimizer.set_jit(True)
            print("[XLA] JIT enabled")
        else:
            tf.config.optimizer.set_jit(False)
            print("[XLA] JIT disabled on macOS (MPS backend)")
    except Exception as exc:
        print(f"[XLA] warning: {exc}")


def get_strategy_auto() -> tf.distribute.Strategy:
    """
    Return a MirroredStrategy when 2+ GPUs are detected, otherwise the default
    (single-device) strategy. MirroredStrategy synchronises gradients across
    GPUs using all-reduce, scaling batch size linearly with the number of GPUs.
    """
    if len(tf.config.list_logical_devices("GPU")) >= 2:
        return tf.distribute.MirroredStrategy()
    return tf.distribute.get_strategy()


