def _decode_mammo_tf(path: tf.Tensor, grayscale: bool) -> tf.Tensor:
    """
    Decode an image file into a float32 tensor of shape [H, W, 3] inside the
    TensorFlow graph (used in the tf.data map pipeline).

    If `grayscale=True`, the image is decoded as a single channel and then
    repeated three times along the channel axis. This preserves the grayscale
    nature of mammograms while satisfying the model's 3-channel input requirement.
    """
    raw = tf.io.read_file(path)
    if grayscale:
        img = tf.image.decode_image(raw, channels=1, expand_animations=False)
        img = tf.image.convert_image_dtype(img, tf.float32)
        img.set_shape([None, None, 1])
        img = tf.repeat(img, 3, axis=-1)   # [H, W, 1] → [H, W, 3]
    else:
        img = tf.image.decode_image(raw, channels=3, expand_animations=False)
        img = tf.image.convert_image_dtype(img, tf.float32)
        img.set_shape([None, None, 3])
    return img


def _decode_mammo_pil(path: str, grayscale: bool) -> np.ndarray:
    """
    Decode an image file into a float32 numpy array of shape [H, W, 3] using PIL.
    Used inside the Python generator for the deterministic validation dataset,
    where TF graph execution is not available.
    """
    img = Image.open(path)
    if grayscale:
        arr = np.asarray(img.convert("L"), dtype=np.float32) / 255.0
        arr = arr[:, :, np.newaxis]
        arr = np.repeat(arr, 3, axis=-1)
    else:
        arr = np.asarray(img.convert("RGB"), dtype=np.float32) / 255.0
    return arr


