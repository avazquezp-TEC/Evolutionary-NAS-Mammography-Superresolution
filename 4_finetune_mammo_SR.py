"""
finetune_mammo_SR.py
====================
Stage 2 Finetuning for mammography super-resolution.

Starting Point : A .keras model pre-trained on DIV2K (Stage 2 from TrainSR_GPU.py)
Target Domain  : Mammography images (mammo-bench)
Train Directory: DATASET/mammo_train/   (generated by 1_select_mammo_images.py)
Val Directory  : DATASET/mammo_val/

LR Degradation — Med-BSR (Shao et al. 2023) + 6 MST methods:
  BLUR  : Isotropic (B_iso) or anisotropic (B_aniso) Gaussian — randomized per patch.
  NOISE : Additive Gaussian (grayscale / per-channel / generalized, prob 2/5, 2/5, 1/5).
  DOWN  : 6 MST methods (Nearest · Bilinear · Bicubic · Lanczos · Box · Hamming).
  ORDER : Random combination of the 3 steps (random select/combine strategy).

  - TRAIN : Fully randomized parameters and execution order per patch (via tf.py_function).
  - VAL   : Deterministic round-robin across the 6 MST methods (fixed blur→noise→down order)
            → ensures uniform coverage and perfect reproducibility across epochs.

Usage:
  python finetune_mammo_SR.py \\
      --model_path outputs4x/.../stage2_p128_best.keras \\
      --scale 4 --grayscale
"""

import argparse
import csv
import datetime
import gc
import json
import os
import time
from pathlib import Path

import numpy as np
import tensorflow as tf
from mammo_degradation import make_degradation_fn

# Standard Keras aliases for clarity
keras = tf.keras
layers = tf.keras.layers
AUTOTUNE = tf.data.AUTOTUNE
ALLOWED_EXTS = (".png", ".jpg", ".jpeg", ".bmp", ".webp")

# MST Constants matching mammo_degradation
MST_NAMES = ["nearest", "bilinear", "bicubic", "lanczos", "box", "hamming"]
N_MST = len(MST_NAMES)


def parse_args() -> argparse.Namespace:
    """Parses command line arguments for the finetuning pipeline."""
    p = argparse.ArgumentParser(description="Finetune DIV2K SR model on Mammographies")
    p.add_argument("--model_path", type=str, required=True,
                   help="Path to pre-trained .keras model or a folder containing multiple models")
    p.add_argument("--train_csv", type=str, default="DATASET/mammo_train.csv")
    p.add_argument("--val_csv", type=str, default="DATASET/mammo_val.csv")
    p.add_argument("--out_root", type=str, default="FINETUNE_RESULTS")
    
    p.add_argument("--scale", type=int, default=4, help="Upscaling factor (2 or 4)")
    p.add_argument("--patch_size", type=int, default=128, help="HR patch size during training")
    p.add_argument("--batch_size", type=int, default=8)
    p.add_argument("--lr", type=float, default=2e-5, help="Finetuning learning rate")
    p.add_argument("--max_epochs", type=int, default=50)
    p.add_argument("--es_patience", type=int, default=8, help="Early stopping patience")
    p.add_argument("--es_min_delta", type=float, default=0.01, help="Minimum delta for PSNR improvement")
    p.add_argument("--seed", type=int, default=42)
    p.add_argument("--cosine_lr", action="store_true", help="Use Cosine Decay learning rate scheduler")
    p.add_argument("--cpu_mode", action="store_true", help="Force execution on CPU")
    return p.parse_args()


def configure_runtime(cpu_mode: bool = False) -> None:
    """Configures hardware execution parameters and mixed precision policy."""
    print(f"\n[RUNTIME] Configuring hardware (cpu_mode={cpu_mode})...")
    if cpu_mode:
        os.environ["CUDA_VISIBLE_DEVICES"] = "-1"
        tf.config.set_visible_devices([], "GPU")
        print("  -> Forced CPU mode execution.")
    else:
        gpus = tf.config.list_physical_devices("GPU")
        if gpus:
            try:
                for gpu in gpus:
                    tf.config.experimental.set_memory_growth(gpu, True)
                print(f"  -> Found {len(gpus)} GPU(s). Memory growth activated.")
            except RuntimeError as e:
                print(f"  -> GPU config error: {e}")
        else:
            print("  -> No GPUs found. Falling back to CPU.")

    # Apply Mixed Precision policy to accelerate GPU computing
    try:
        policy = tf.keras.mixed_precision.Policy("mixed_float16")
        tf.keras.mixed_precision.set_global_policy(policy)
        print(f"  -> Mixed precision set to: {policy.name}")
    except Exception as e:
        print(f"  -> Mixed precision setup failed: {e}")


