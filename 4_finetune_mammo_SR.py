# Custom Metrics and Logging Callbacks
# =============================================================================

@tf.keras.utils.register_keras_serializable(package="sr")
def psnr_metric(y_true: tf.Tensor, y_pred: tf.Tensor) -> tf.Tensor:
    """Computes peak signal-to-noise ratio over localized batches."""
    return tf.image.psnr(y_true, y_pred, max_val=1.0)


class CustomProgressLogger(keras.callbacks.Callback):
    """Logs clean epoch breakdowns to the terminal console."""
    def __init__(self, total_epochs: int):
        super().__init__()
        self.total_epochs = total_epochs

    def on_epoch_end(self, epoch: int, logs: dict | None = None) -> None:
        logs = logs or {}
        loss = logs.get("loss", np.nan)
        psnr = logs.get("psnr_metric", np.nan)
        val_loss = logs.get("val_loss", np.nan)
        val_psnr = logs.get("val_psnr_metric", np.nan)
        print(
            f"    Epoch {epoch + 1:02d}/{self.total_epochs:02d} -> "
            f"Loss: {loss:.5f} | PSNR: {psnr:.2f} dB || "
            f"Val Loss: {val_loss:.5f} | Val PSNR: {val_psnr:.2f} dB"
        )


# =============================================================================
# Core Finetuning Engine for a Single Model
# =============================================================================

def finetune_one(
    model_path: str,
    train_ds: tf.data.Dataset,
    val_ds: tf.data.Dataset,
    train_steps: int,
    val_steps: int,
    out_dir: str,
    model_name: str,
    lr: float,
    max_epochs: int,
    es_patience: int,
    es_min_delta: float,
    strategy: tf.distribute.Strategy,
    use_cosine_lr: bool = False,
) -> tuple[float, str, str]:
    """
    Runs the finetuning loop for a given model.
    
    Returns:
        tuple: (best_val_psnr, path_to_best_saved_model, parameters_summary_string)
    """
    # Safeguard to prevent layer naming collision crashes across loop iterations
    keras.backend.clear_session()
    gc.collect()

    # Initialize execution environment context
    with strategy.scope():
        # Custom layer registry tracking references required for load reconstruction
        from TrainSR_GPU import PixelShuffle
        try:
            model = keras.models.load_model(
                model_path,
                custom_objects={"PixelShuffle": PixelShuffle, "psnr_metric": psnr_metric},
                compile=False
            )
            print(f"  -> Model successfully loaded into memory scope.")
        except Exception as e:
            print(f"  -> Critical load error on {model_path}: {e}")
            return 0.0, "ERROR", str(e)

        # Dynamic Learning Rate adjustments
        if use_cosine_lr:
            lr_schedule = keras.optimizers.schedules.CosineDecay(
                initial_learning_rate=lr,
                decay_steps=max_epochs * train_steps,
                alpha=0.01
            )
            opt = keras.optimizers.Adam(learning_rate=lr_schedule)
            lr_desc = f"CosineDecay(init={lr})"
        else:
            opt = keras.optimizers.Adam(learning_rate=lr)
            lr_desc = f"Constant({lr})"

        model.compile(optimizer=opt, loss="mae", metrics=[psnr_metric])

    best_model_path = os.path.join(out_dir, f"ft_{model_name}_best.keras")

    callbacks = [
        keras.callbacks.ModelCheckpoint(
            best_model_path,
            monitor="val_psnr_metric",
            mode="max",
            save_best_only=True,
            verbose=0
        ),
        keras.callbacks.EarlyStopping(
            monitor="val_psnr_metric",
            mode="max",
            patience=es_patience,
            min_delta=es_min_delta,
            restore_best_weights=True,
            verbose=1
        ),
        CustomProgressLogger(total_epochs=max_epochs)
    ]

    print(f"  -> Training updates configured. Steps per epoch: {train_steps} | Steps Val: {val_steps}")
    start_t = time.time()
    
    history = model.fit(
        train_ds,
        validation_data=val_ds,
        epochs=max_epochs,
        steps_per_epoch=train_steps,
        validation_steps=val_steps,
        callbacks=callbacks,
        verbose=0
    )
    
    elapsed = time.time() - start_t
    psnr_hist = history.history.get("val_psnr_metric", [0.0])
    best_val_psnr = float(np.nanmax(psnr_hist) if not np.all(np.isnan(psnr_hist)) else 0.0)
    
    print(f"  -> Execution complete in {elapsed/60:.2f} mins. Peak Val PSNR: {best_val_psnr:.3f} dB")

    params_summary = {
        "lr_strategy": lr_desc,
        "base_lr": lr,
        "max_epochs": max_epochs,
        "actual_epochs": len(psnr_hist),
        "runtime_sec": round(elapsed, 2)
    }

    return best_val_psnr, best_model_path, json.dumps(params_summary)


# =============================================================================
# Main Orchestrator Execution Flow
# =============================================================================

def main() -> None:
    args = parse_args()
    configure_runtime(cpu_mode=args.cpu_mode)

    # Generate timestamped logging results path
    timestamp = datetime.datetime.now().strftime("%Y%m%d_%H%M%S")
    root = os.path.join(args.out_root, f"ft_run_{timestamp}")
    os.makedirs(root, exist_ok=True)

    print(f"\n{'='*75}")
    print(" DOMAIN FINETUNING PIPELINE — MAMMO SUPER-RESOLUTION")
    print(f"{'='*75}")
    print(f"  Model input target : {args.model_path}")
    print(f"  Train CSV source   : {args.train_csv}")
    print(f"  Val CSV source     : {args.val_csv}")
    print(f"  Output directory   : {root}")
    print(f"  Configuration      : Scale=×{args.scale} | Patch={args.patch_size} | Batch={args.batch_size}")
    print(f"  Hyperparameters    : LR={args.lr} | Epochs={args.max_epochs} | Cosine={args.cosine_lr}")
    print(f"='*75\n")

    # ── Discover Candidate Input Models ──────────────────────────────────────
    models_to_run = []
    if os.path.isdir(args.model_path):
        print(f"[SEARCH] scanning directory for models: {args.model_path}")
        for file in sorted(os.listdir(args.model_path)):
            if file.endswith(".keras"):
                full_p = os.path.join(args.model_path, file)
                name = Path(file).stem
                models_to_run.append((name, full_p))
    elif os.path.isfile(args.model_path):
        name = Path(args.model_path).stem
        models_to_run.append((name, args.model_path))
    else:
        raise FileNotFoundError(f"Target path option invalid or missing: {args.model_path}")

    if not models_to_run:
        print("  -> WARNING: No valid .keras files discovered. System exiting.")
        return
    print(f"  -> Discovered {len(models_to_run)} model candidate profiles ready for training.")

    # ── Initialize decoupled data streams ────────────────────────────────────
    print("\n[DATA] Loading and parsing target datasets...")
    train_ds, n_train = make_finetune_dataset(
        args.train_csv, args.scale, args.patch_size, args.batch_size, is_training=True, seed=args.seed
    )
    val_ds, n_val = make_finetune_dataset(
        args.val_csv, args.scale, args.patch_size, args.batch_size, is_training=False, seed=args.seed
    )

    train_steps = int(np.ceil(n_train / args.batch_size))
    val_steps = int(np.ceil(n_val / args.batch_size))
    print(f"  -> Datasets loaded. Train samples: {n_train} | Val samples: {n_val}")

    # Set up distributed GPU compute strategy
    strategy = tf.distribute.get_strategy()  # Default system single-scope mapping fallback

    # Prepare master search report summary tracking file
    summary_path = os.path.join(root, "finetune_summary.csv")
    fieldnames = ["model_name", "source_model", "params", "best_val_psnr", "saved_full_path"]
    with open(summary_path, "w", newline="", encoding="utf-8") as f:
        csv.DictWriter(f, fieldnames=fieldnames).writeheader()

    # ── Finetuning Loop ──────────────────────────────────────────────────────
    for model_name, model_path in models_to_run:
        print(f"\n{'='*60}")
        print(f" FINETUNING: {model_name}")
        print(f"  Source Path: {model_path}")
        print(f"{'='*60}")

        best_psnr, ft_model_path, params = finetune_one(
            model_path=model_path,
            train_ds=train_ds,
            val_ds=val_ds,
            train_steps=train_steps,
            val_steps=val_steps,
            out_dir=root,
            model_name=model_name,
            lr=args.lr,
            max_epochs=args.max_epochs,
            es_patience=args.es_patience,
            es_min_delta=args.es_min_delta,
            strategy=strategy,
            use_cosine_lr=args.cosine_lr,
        )

        # Append performance metrics to the global master CSV sheet
        with open(summary_path, "a", newline="", encoding="utf-8") as f:
            csv.DictWriter(f, fieldnames=fieldnames).writerow({
                "model_name": model_name,
                "source_model": model_path,
                "params": params,
                "best_val_psnr": best_psnr,
                "saved_full_path": os.path.abspath(ft_model_path),
            })

    print(f"\n{'='*75}")
    print(" FINETUNING PROCESS COMPLETED SUCCESSFULLY")
    print(f"  Master Report spreadsheet logs located at: {os.path.abspath(summary_path)}")
    print(f"{'='*75}\n")


if __name__ == "__main__":
    main()