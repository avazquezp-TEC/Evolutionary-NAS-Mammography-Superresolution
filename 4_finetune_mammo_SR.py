def load_csv_paths(csv_path: str) -> list[str]:
    """Loads valid file paths from a metadata CSV."""
    if not os.path.isfile(csv_path):
        raise FileNotFoundError(f"Dataset CSV not found: {csv_path}")
    paths = []
    with open(csv_path, mode="utf-8", errors="ignore") as f:
        reader = csv.DictReader(f)
        for row in reader:
            p = row.get("full_path", "").strip()
            if p and os.path.isfile(p) and p.lower().endswith(ALLOWED_EXTS):
                paths.append(p)
    return paths


def make_finetune_dataset(
    csv_path: str,
    scale: int,
    patch_size: int,
    batch_size: int,
    is_training: bool = True,
    seed: int = 42,
) -> tuple[tf.data.Dataset, int]:
    """
    Creates an optimized pipeline supporting Med-BSR degradation.
    
    - Train: Randomizes downsampling methods internally per patch via a random seed.
    - Val: Applies a stable round-robin sequence to ensure consistent validation.
    """
    file_paths = load_csv_paths(csv_path)
    n_images = len(file_paths)
    if n_images == 0:
        raise ValueError(f"No valid image files found in: {csv_path}")

    ds = tf.data.Dataset.from_tensor_slices(file_paths)
    if is_training:
        ds = ds.shuffle(buffer_size=max(200, n_images), seed=seed, reshuffle_each_iteration=True)

    # 1) Image decoding stage
    def _read_and_decode(path_tensor):
        img_bytes = tf.io.read_file(path_tensor)
        img = tf.image.decode_image(img_bytes, channels=3, expand_animations=False)
        img = tf.image.convert_image_dtype(img, tf.float32)  # Automatically normalizes to [0, 1]
        return img

    ds = ds.map(_read_and_decode, num_parallel_calls=AUTOTUNE)

    # 2) Safe patch cropping stage
    def _extract_crop(img):
        shape = tf.shape(img)
        h, w = shape[0], shape[1]
        target_h = tf.maximum(h, patch_size + 2)
        target_w = tf.maximum(w, patch_size + 2)
        padded = tf.image.resize_with_crop_or_pad(img, target_h, target_w)
        
        if is_training:
            patch = tf.image.random_crop(padded, [patch_size, patch_size, 3], seed=seed)
            patch = tf.image.random_flip_left_right(patch, seed=seed)
            patch = tf.image.random_flip_up_down(patch, seed=seed)
        else:
            patch = tf.image.resize_with_crop_or_pad(padded, patch_size, patch_size)
        return patch

    ds = ds.map(_extract_crop, num_parallel_calls=AUTOTUNE)

    # 3) Degradation function factory integration
    deg_fn = make_degradation_fn(scale=scale, seed=seed)
    lr_size = patch_size // scale

    if is_training:
        def _apply_train_deg(hr_patch):
            # Dynamic downsampling method (-1 flags fully randomized degradation parameters)
            method_seed = tf.random.uniform([], minval=-1, maxval=N_MST, dtype=tf.int32)
            
            def _py_wrapper(hr_t, method_t):
                return deg_fn(hr_t.numpy(), int(method_t.numpy()))
            
            lr_patch = tf.py_function(_py_wrapper, [hr_patch, method_seed], tf.float32)
            lr_patch.set_shape([lr_size, lr_size, 3])
            return lr_patch, hr_patch

        ds = ds.map(_apply_train_deg, num_parallel_calls=AUTOTUNE)
    else:
        # Validation setup: Round-Robin distribution strategy over the 6 MST filters
        indices = list(range(n_images))
        idx_ds = tf.data.Dataset.from_tensor_slices(indices)
        
        zipped_ds = tf.data.Dataset.zip((ds, idx_ds))

        def _apply_val_deg(hr_patch, idx_tensor):
            method_idx = idx_tensor % N_MST  # Cyclic distribution (0, 1, 2, 3, 4, 5, 0, ...)
            
            def _py_wrapper(hr_t, method_t):
                return deg_fn(hr_t.numpy(), int(method_t.numpy()))
            
            lr_patch = tf.py_function(_py_wrapper, [hr_patch, method_idx], tf.float32)
            lr_patch.set_shape([lr_size, lr_size, 3])
            return lr_patch, hr_patch

        ds = zipped_ds.map(_apply_val_deg, num_parallel_calls=AUTOTUNE)

    ds = ds.batch(batch_size).prefetch(buffer_size=AUTOTUNE)
    return ds, n_images


